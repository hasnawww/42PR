#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 48 && c <= 57) || c == 35 || c == 42 || (c >= 65 && c <= 90)
		|| (c >= 97 && c <= 122))
	{
		return (1);
	}
	return (0);
}

